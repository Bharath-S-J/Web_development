// Define a function named add that takes two parameters n1 and n2 and returns their sum
function add(n1, n2) {
    return n1 + n2;
}

// Export another function named sub that takes two parameters n1 and n2 and returns their difference
export function sub(n1, n2) {
    return n1 - n2;
}

// Call the add function with arguments 5 and 5, and display the result using alert
alert(add(5, 5));

// Call the sub function with arguments 5 and 5, and display the result using alert
alert(sub(5, 5));
